package _Topics.Java;

// Interface is a reference type. A class implements an interface, thereby inheriting the abstract methods of the interface.
// By default interface methods are abstract and public and variables are public, static and final.
// Interface can have only abstract methods.
// 
public interface Interfaces {
  // Interface methods are abstract and public by default.
  void print();
  // Interface variables are public, static and final by default.
  int x = 10;
}